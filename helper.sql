 from INFORMATION_SCHEMA.COLUMNS c
 LEFT JOIN  INFORMATION_SCHEMA.TABLE_CONSTRAINTS  CONSTRAINTS ON CONSTRAINTS.TABLE_NAME = C.TABLE_NAME
