                                                         AND CONSTRAINTS.CONSTRAINT_SCHEMA = K.CONSTRAINT_SCHEMA
                                                         AND CONSTRAINTS.CONSTRAINT_NAME = K.CONSTRAINT_NAME
														   AND K.COLUMN_NAME = C.COLUMN_NAME AND CONSTRAINTS.CONSTRAINT_TYPE = 'PRIMARY KEY'
