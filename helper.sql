select  DISTINCT
C.COLUMN_NAME, 
		DATA_TYPE +  (CASE
		WHEN DATA_TYPE = 'varchar' THEN  '(' + CAST(CHARACTER_MAXIMUM_LENGTH AS VARCHAR(20)) + ')'
		WHEN DATA_TYPE= 'decimal' THEN  '(' + CAST(NUMERIC_PRECISION AS VARCHAR(20))+ ','+ CAST(NUMERIC_SCALE AS VARCHAR(20)) + ')'
		ELSE '' END),
		CASE WHEN C.IS_NULLABLE = 'YES' THEN '' else 'x' end
 from INFORMATION_SCHEMA.COLUMNS c
 where C.table_name ='Securities'


